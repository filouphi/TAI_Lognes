            <h3 class="text-center">ORIGINE</h3>
            <p>Robert Nesta MarleyNote 1 est né le 6 février 1945 à Rhoden Hall, près de Nine Miles dans la paroisse de Saint Ann.
					Bob Marley est né d'une mère noire jamaïcaine (afro-caribéenne) âgée de 18 ans, Cedella Booker, épouse Marley Booker (1926-2008), et d'un père blanc d'origine anglaise né en Jamaïque, Norval Sinclair Marley (1885–1955) qui prétendait être capitaine de la Royal Navy, mais qui était en réalité contre-maître dans les plantations. Robert a très peu connu son père souvent en voyage mais qui apporte son soutien financier à Cedella et son fils2. Ses grands-parents paternels sont Albert Thomas Marley, un anglais du Sussex et Ellen Broomfield une bourgeoise jamaïcaine colorée. D'après Michael George Marley (fils de Noel Marley, frère de Norval Marley), qui l'aurait appris de sa famille puis vérifié, les Marley seraient des Juifs syriens passés par l'Angleterre avant de s'installer en Jamaïque.</p>
