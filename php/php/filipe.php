            <h3 class="text-center">Periode rocksteady</h3>
            <p>Vers le milieu des années 1960, le rocksteady succède au ska. Plus lent et chaloupé que ce dernier, le rocksteady marque une étape dans l'évolution de la musique jamaïcaine, qui s'affranchit de plus en plus des rythmiques rapides du ska. Reprenant la soul nord-américaine et le rhythm and blues, le style est marqué par plus de chants et de claviers, et moins de cuivre. Les chansons reprennent des thèmes d'amour et de religion et les paroles s'imprègnent peu à peu de croyances rastafari. Les chanteurs s'adressent à la jeunesse et aux rudes boys des ghettos, et tentent de leur redonner espoir5.

					Le lendemain de son mariage avec Rita Anderson le 11 février 19666, Bob Marley, à la demande de sa mère, remariée avec un Jamaïcain nommé Booker installé à Wilmington dans le Delaware, part la rejoindre aux États-Unis. Bob y travaille à l'hôtel Dupont, mais continue à écrire des chansons. Il est provisoirement remplacé par Constantine « Dream » Walker. Souhaitant retrouver sa liberté, il retourne dans son île après l'été 1966. Il s'intéresse de plus en plus au mouvement rastafari, qui a émergé dans les années 1930 en Jamaïque, et fonde avec Peter Tosh et Bunny Livingston le label indépendant Wail'n Soul'm. Leur premier titre, autoproduit, dans le nouveau style rocksteady, s'intitule Bend Down Low. À Kingston, Mortimer Planno, un rasta jamaïcain d'origine cubaine qui a voyagé en Éthiopie et rencontré Haïlé Sélassié Ier au début des années 1960, lui transmet une partie de sa culture rasta. Métis clair rejeté par les noirs jamaïcains, Bob Marley se sent accepté par ce mouvement7. Sans le soutien d'un distributeur professionnel, ses disques se vendent très mal, et Bob Marley est trop pauvre pour vivre en ville avec sa femme Rita et ses deux enfants Cedella (en) et Ziggy. Il retourne dans son village natal en 1967 pour un ressourcement spirituel, mais continue à enregistrer et à publier nombre de 45 tours obscurs pour sa petite marque Wail'n Soul'm, comme les futurs classiques Hypocrites et Nice Time, qui sortent sous le nom de Bob Marley and the Wailers.

					Rita, Bob Marley et Peter Tosh rencontrent, en janvier 1968, le chanteur américain Johnny Nash, qui est décidé à lancer le style rocksteady aux États-Unis, et son manager Danny Sims, avec qui ils signent un contrat international exclusif pour les disques et éditions JAD. Bob Marley leur fournit quantité de compositions inédites, dont Stir It Up, qui deviendra bientôt un succès pour Nash. Johnny Nash a beaucoup de succès avec le rocksteady (tube américain Hold Me Tight en 1968), mais l'album de Bob Marley and the Wailers qu'il a financé ne sort pas (il ne sera finalement publié qu'en 1997 chez JAD). Seule une nouvelle version de Bend Down Low avec des cuivres américains ajoutés à New York sort en France et au Canada (JAD-CBS) en 1968, mais sans aucun succès. Bob Marley écrit parallèlement son premier morceau rasta, Selassie Is the Chapel, en 1968. Cet enregistrement important, dans le style nyabinghi (tambours rastas), est financé par Mortimer Planno, qui en interprète la face B, A Little Prayer. Quelques producteurs locaux se succèdent, mais le trio vocal n'a plus aucun succès depuis son départ du giron de Coxsone Dodd.</p>
