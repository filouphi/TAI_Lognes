            <h3 class="text-center">periode ska</h3>
            <p>À l'adolescence, Bob Marley quitte la misère de la campagne pour celle du ghetto de Trenchtown à Kingston. Ayant arrêté l'école à 14 ans, il y rencontre Neville Livingston, dit Bunny Wailer, et Winston Hubert McIntosh, dit Peter Tosh, avec qui il chante des cantiques et des succès de soul américaine qu'ils entendent sur les radios de Miami. Le chanteur Joe Higgs leur donne des cours de chant. Bob Marley enregistre sa première chanson Judge Not pour le producteur Leslie Kong, du label Beverley's, en 1962, à l'âge de 17 ans, ainsi qu'une reprise d'un succès de country de Claude Gray : One Cup of Coffee en 1962. Ces titres n'ont aucun succès, mais Bob continue à s'investir dans la musique.

				En 1963, il forme avec Junior Braithwaite, Peter Tosh et Bunny Wailer, le groupe vocal The Wailers (les gémisseurs). Ils obtiennent un contrat avec Studio One en 1964 et leurs premiers morceaux de ska, gospel, rhythm and blues et soul sont produits par le grand producteur local Clement « Sir Coxsone » Dodd, qui a pour assistant en studio Lee « Scratch » Perry (Scratch assure aussi la promotion). Coxsone Dodd et les Wailers signent un contrat pour cinq ans. En février 1964, le titre Simmer Down cartonne et devient no 1 en Jamaïque : 80 000 copies du titre sont vendues. Les Wailers enregistrent Rude Boy, I'm Still Waiting, Put It On et une première version de One Love. Bob quitte son métier de soudeur. Après l'album intitulé The Wailin' Wailers, le trio se sépare de Coxsonne, qui ne leur a pas versé grand-chose en contrepartie des dizaines de titres qu'ils ont enregistrés pour lui.</p>
